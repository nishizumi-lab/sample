import numpy as np

# 2銘柄 × 3日分の株価データ
# 行：銘柄A・銘柄B
# 列：1日目・2日目・3日目
prices = np.array([[100, 102, 101],   # 銘柄A
                   [ 98, 103,  99]])  # 銘柄B

# 銘柄Aの2日目の株価（行0, 列1）
print(prices[0, 1])  # 102

# 銘柄Aの全日データ
print(prices[0, :])  # [100 102 101]

# 3日目（列2）の全銘柄の株価
print(prices[:, 2])  # [101  99]

# スライスで参照（銘柄A〜B、2〜3日目）
print(prices[0:2, 1:3])
# [[102 101]
#  [103  99]]

# 銘柄Aの3日目の株価を修正
prices[0, 2] = 999
print(prices)
# [[100 102 999]
#  [ 98 103  99]]

# 銘柄Bの1〜2日目の株価を一括で書き換え
prices[1, 0:2] = [200, 201]
print(prices)
# [[100 102 999]
#  [200 201  99]]